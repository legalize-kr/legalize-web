"use strict";

const fs = require("fs");
const path = require("path");
const { generateRedirects } = require("./scripts/generate-precedent-redirects.js");

module.exports = function(eleventyConfig) {
  // 기존 HTML 파일을 passthrough로 처리 (템플릿으로 해석하지 않음)
  eleventyConfig.addPassthroughCopy("index.html");
  eleventyConfig.addPassthroughCopy("about.html");
  eleventyConfig.addPassthroughCopy("laws.html");
  eleventyConfig.addPassthroughCopy("precedents.html");
  eleventyConfig.addPassthroughCopy("ordinances.html");
  eleventyConfig.addPassthroughCopy("admrules.html");
  eleventyConfig.addPassthroughCopy("usage.html");
  eleventyConfig.addPassthroughCopy("404.html");
  eleventyConfig.addPassthroughCopy({
    "assets/og-image.png": "assets/og-image.png",
    "assets/usage/legalq.png": "assets/usage/legalq.png",
    "assets/usage/ct-llm.png": "assets/usage/ct-llm.png",
    "assets/usage/graphify-youtube.png": "assets/usage/graphify-youtube.png",
  });
  eleventyConfig.addPassthroughCopy("CNAME");
  eleventyConfig.addPassthroughCopy("robots.txt");
  // sitemap.xml is now generated by sitemap.njk (new precedent URLs included)
  eleventyConfig.addPassthroughCopy("llms.txt");
  eleventyConfig.addPassthroughCopy(".nojekyll");

  for (const statsFile of [
    "stats-laws.json",
    "stats-precedents.json",
    "stats-ordinances.json",
    "stats-admrules.json",
  ]) {
    if (fs.existsSync(path.join(__dirname, statsFile))) {
      eleventyConfig.addPassthroughCopy(statsFile);
    }
  }

  // CSS 및 이미지 뷰어 페이지
  eleventyConfig.addPassthroughCopy("css");
  eleventyConfig.addPassthroughCopy({ "images/index.html": "images/index.html" });

  // 샤딩된 데이터 파일 (public/data/ → _site/data/)
  eleventyConfig.addPassthroughCopy({ "public/data": "data" });

  // 빌드 후 legacy-paths.json 기반 meta-refresh redirect HTML 생성
  // GitHub Pages는 _redirects 미지원 → 정적 HTML로 redirect 구현
  eleventyConfig.on("eleventy.after", async ({ dir }) => {
    const siteDir = (dir && dir.output) || path.join(__dirname, "_site");
    const legacyPathsFile = path.join(__dirname, "legacy-paths.json");
    generateRedirects(siteDir, legacyPathsFile);
  });

  return {
    templateFormats: ["njk"],
    dir: {
      input: ".",
      output: "_site",
      includes: "_includes",
      data: "_data",
    },
  };
};
